public class Datatype {
    public static void main (String[] args) {
        byte b = 8;
        System.out.println(b);

        char c = '#';
        System.out.println(c);

        boolean var = true;
        System.out.println(var);

        float f = 10.5F;
        System.out.println(f);

        int num = 21;
        System.out.println(num);
    }
}
